// c'de değişken tanımlama

#include <stdio.h>

int main() {
    int sayi = 10;       // Tamsayı değişken
    float ondalik = 5.5; // Ondalıklı sayı değişkeni
    char harf = 'A';     // Tek karakter değişkeni
    printf("Sayi: %d, Ondalik: %.2f, Harf: %c\n", sayi, ondalik, harf);
    return 0;
}
