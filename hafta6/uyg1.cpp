// c++'da değişken tanımlama

#include <iostream>
using namespace std;

int main() {
    int sayi = 10;       // Tamsayı değişken
    float ondalik = 5.5; // Ondalıklı sayı değişkeni
    char harf = 'A';     // Tek karakter değişkeni

    cout << "Sayi: " << sayi << ", Ondalik: " << ondalik << ", Harf: " << harf << endl;
    return 0;
}
